/* qa-translations.js — automated QA to seed the native-speaker review queue.
   For every baked locale + non-English language, flags translatable strings that
   look UNTRANSLATED: identical to the English source AND still containing Latin
   letters (a strong signal for es/vi/hi and for ar/ur/zh, where genuine
   translations shouldn't be Latin script). Borrowed terms (HTTPS, cookies, URLs)
   may show up and are usually fine — this is a review aid, not a hard gate.
   Writes docs/translation-qa.md and prints a per-language summary.
   Run: node scripts/qa-translations.js
*/
'use strict';
const fs = require('fs');
const path = require('path');
const ROOT = path.join(__dirname, '..');
const NONEN = ['es', 'vi', 'ar', 'hi', 'ur', 'zh'];
const BANDS = { grade35: [3, 4, 5], grade68: [6, 7, 8] };

function loadBreakout(band, g) {
  const p = path.join(ROOT, band, 'locales', 'ai-grade' + g + '.js');
  return new Function('window', fs.readFileSync(p, 'utf8') + '\nreturn window.BREAKOUT;')({});
}
const hasLatin = s => /[A-Za-z]{3,}/.test(String(s));       // 3+ Latin letters in a row
const stripEmoji = s => String(s).replace(/[\p{Emoji}←-⇿☀-➿️‍]/gu, '').trim();

// collect (label, enValue) translatable pairs from an activity's EN content + chrome
function enStrings(B) {
  const out = [];
  const U = B.UI.en;
  ['header.h1', 'header.sub', 'brief.h', 'brief.p', 'win.h', 'win.p'].forEach(k => out.push([`chrome:${k}`, U[k]]));
  B.CONTENT.en.clues.forEach((c, i) => { ['nm', 'title', 'body'].forEach(f => out.push([`clue${i + 1}.${f}`, c[f]])); });
  B.CONTENT.en.locks.forEach((l, i) => {
    ['title', 'q', 'reason'].forEach(f => out.push([`L${i + 1}.${f}`, l[f]]));
    if (l.type === 'mc') l.options.forEach((o, j) => out.push([`L${i + 1}.opt${j}`, o]));
    if (l.type === 'multi') l.items.forEach((it, j) => out.push([`L${i + 1}.item${j}`, it.t]));
    if (l.type === 'seq') l.pads.forEach((p, j) => out.push([`L${i + 1}.pad${j}`, p.e]));
  });
  return out;
}
function langStrings(B, lg) {
  const out = {};
  const U = B.UI[lg];
  ['header.h1', 'header.sub', 'brief.h', 'brief.p', 'win.h', 'win.p'].forEach(k => (out[`chrome:${k}`] = U[k]));
  const C = B.CONTENT[lg];
  if (!C || !C.clues.length) return null;
  C.clues.forEach((c, i) => ['nm', 'title', 'body'].forEach(f => (out[`clue${i + 1}.${f}`] = c[f])));
  C.locks.forEach((l, i) => {
    ['title', 'q', 'reason'].forEach(f => (out[`L${i + 1}.${f}`] = l[f]));
    if (l.type === 'mc') l.options.forEach((o, j) => (out[`L${i + 1}.opt${j}`] = o));
    if (l.type === 'multi') l.items.forEach((it, j) => (out[`L${i + 1}.item${j}`] = it.t));
    if (l.type === 'seq') l.pads.forEach((p, j) => (out[`L${i + 1}.pad${j}`] = p.e));
  });
  return out;
}

const report = ['# Translation QA — native-review queue', '',
  'Auto-generated by `scripts/qa-translations.js`. Each row is a string whose',
  'translation is **identical to English and still contains Latin letters** — a',
  'likely-untranslated leftover. Borrowed terms (HTTPS, cookies, proper nouns)',
  'can appear here and may be intentional. Structure is verified separately by',
  '`solve-test.js`; this file is only about wording.', ''];
const perLang = Object.fromEntries(NONEN.map(l => [l, 0]));
let totalFlagged = 0, totalChecked = 0;

for (const [band, grades] of Object.entries(BANDS)) {
  for (const g of grades) {
    const B = loadBreakout(band, g);
    const en = enStrings(B);
    for (const lg of NONEN) {
      const L = langStrings(B, lg);
      if (!L) { report.push(`- **ai-grade${g} [${lg}]**: not translated (falls back to English).`); continue; }
      const flags = [];
      for (const [label, enVal] of en) {
        const tv = L[label];
        totalChecked++;
        if (tv == null) continue;
        const a = stripEmoji(enVal), b = stripEmoji(tv);
        if (a && a === b && hasLatin(b)) { flags.push([label, tv]); perLang[lg]++; totalFlagged++; }
      }
      if (flags.length) {
        report.push('', `### ai-grade${g} · ${lg} — ${flags.length} to review`, '', '| Field | Value (identical to EN) |', '|---|---|');
        flags.forEach(([label, v]) => report.push(`| \`${label}\` | ${String(v).replace(/\|/g, '\\|').slice(0, 90)} |`));
      }
    }
  }
}

report.splice(6, 0,
  '', '## Summary', '',
  '| Language | Strings flagged for review |', '|---|---|',
  ...NONEN.map(l => `| ${l} | ${perLang[l]} |`),
  `| **total** | **${totalFlagged}** of ${totalChecked} checked |`, '');

const docsDir = path.join(ROOT, 'docs');
if (!fs.existsSync(docsDir)) fs.mkdirSync(docsDir);
fs.writeFileSync(path.join(docsDir, 'translation-qa.md'), report.join('\n') + '\n');
console.log('Wrote docs/translation-qa.md');
console.log('Flagged (identical-to-EN, has Latin): ' + NONEN.map(l => `${l}=${perLang[l]}`).join('  ') + `  | total ${totalFlagged}/${totalChecked}`);
